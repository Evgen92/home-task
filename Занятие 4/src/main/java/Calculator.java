public class Calculator {

    public int calcSum(int a, int b){
        return a + b;
    }

    public double calcPow(double a, double b){
        return Math.pow(a, b);
    }

    public int calcMult(int a, int b){
        return a * b;
    }

    public int calcMin(int a, int b){
        return Math.min(a, b);
    }

    public int calcMax(int a, int b){
        return Math.max(a, b);
    }
}
